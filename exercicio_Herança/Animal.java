package exercicio_Herança;

public class Animal {
		// Atributos gerais
	//acrescentei o private somente para praticar, esses dados não comprometedores
		private String nome;
		private int idade;
		private String som;
		private int expectativaVida;
		private int patas;
		public int getPatas() {
			return patas;
		}
		public void setPatas(int patas) {
			this.patas = patas;
		}
		public String getNome() {
			return nome;
		}
		public void setNome(String nome) {
			this.nome = nome;
		}
		public int getIdade() {
			return idade;
		}
		public void setIdade(int idade) {
			this.idade = idade;
		}
		public String getSom() {
			return som;
		}
		public void setSom(String som) {
			this.som = som;
		}
		public int getExpectativaVida() {
			return expectativaVida;
		}
		public void setExpectativaVida(int expectativaVida) {
			this.expectativaVida = expectativaVida;
		}
		
		
		
		

	}
