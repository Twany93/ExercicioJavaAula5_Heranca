package exercicio_Herança;

public class Preguiça extends Animal {
	private String devagar;
	private String subeArvore;
	public String getDevagar() {
		return devagar;
	}
	public void setDevagar(String devagar) {
		this.devagar = devagar;
	}
	public String getSubeArvore() {
		return subeArvore;
	}
	public void setSubeArvore(String subeArvore) {
		this.subeArvore = subeArvore;
	}
	
}
